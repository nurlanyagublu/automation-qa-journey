package Day4;

public class SwitchCaseState {

    public static void main(String[] args) {

        int weekno = 1;
        switch (weekno) {
            case 1: System.out.println("Welcome to the Week 1!");
            break;

            case 2: System.out.println("Welcome to the Week 2!");
            break;
            case 3: System.out.println("Welcome to the Week 3!");
            default: System.out.println("Invalid!");
        }
    }
}
